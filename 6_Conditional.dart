void main() {
  var a = 2;
  switch(a) {
    case 0:
    print("ZERO");
    break;

    case 1:
    print("ONE");
    break;

    case 2:
    print('TWO');
    break;

    case 3:
    print('THREE');
    break;

    default:
    print('other');
  }
}